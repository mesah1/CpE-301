.include<m328pbdef.inc>

.EQU STARTADDS = 0x0200 
.ORG 0x0200

	;MATCHING VALUES FROM ADDRESS TO REGISTERS
	LDI XL, LOW(STARTADDS)
	LDI XH, HIGH(STARTADDS)
	LDI YL, LOW(0x0300)
	LDI YH, HIGH(0x0300)
	LDI ZL, LOW(0x0500)
	LDI ZH, HIGH(0x0500)
	;INITIALIZING COUNTER TO R16
	LDI R20, 250
	;STARTING VALUES FROM 10 TO 260 CONSECUTIVELY 
	LDI R21, 10
;STARTING TO LOAD VALUES INTO X-REGISTER 
CALC:
	ST X+, R21
	MOV R22, R21
	RJMP CHECK_DIV

CHECK_DIV:
	SUBI R22, 5
	BRLO NO_DIVIS
	BREQ DIVIS
	RJMP CHECK_DIV

DIVIS:
	ST  Y+, R21
	ADD R16, R21
	ADC R17, R23
	JMP LOOP
NO_DIVIS:
	ST Z+, R21
	ADD R18, R21
	ADC R19, R23
	RJMP LOOP

LOOP:
	INC R21
	DEC R20
	BRNE CALC

ADDITION:
	ADD R16, R18
	ADC R17, R19

END: JMP END
